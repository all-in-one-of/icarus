from Qt import QtCompat, QtCore, QtWidgets
import about_rc  # Import resource file as generated by pyside-rcc


# ----------------------------------------------------------------------------
# Configuration
# ----------------------------------------------------------------------------

# Set the UI and the stylesheet
UI_FILE = "about_ui.ui"


# ----------------------------------------------------------------------------
# Main dialog class
# ----------------------------------------------------------------------------

class aboutDialog(QtWidgets.QMainWindow):
	""" Main dialog class.
	"""
	def __init__(self, parent=None):
		super(aboutDialog, self).__init__(parent)

		# Load UI
		self.ui = QtCompat.load_ui(fname=UI_FILE)

		# Set the main widget
		self.setCentralWidget(self.ui)

		# Set window flags
		self.setWindowFlags(QtCore.Qt.Popup)
		# self.setWindowFlags(QtCore.Qt.FramelessWindowHint | QtCore.Qt.Popup | QtCore.Qt.SplashScreen | QtCore.Qt.WindowStaysOnTopHint)


	def msg(self, msg):
		""" Display message in about dialog.
		"""
		self.ui.aboutMessage_label.setText(msg)

		# Move to centre of active screen
		desktop = QtWidgets.QApplication.desktop()
		screen = desktop.screenNumber(desktop.cursor().pos())
		self.move(desktop.screenGeometry(screen).center() - self.ui.frameGeometry().center())
		self.show()
		self.ui.exec_()


	def mousePressEvent(self, QMouseEvent):
		""" Close about dialog if mouse is clicked.
		"""
		self.hide()

